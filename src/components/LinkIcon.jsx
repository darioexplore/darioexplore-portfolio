/* Detects platform from URL and renders the matching brand SVG icon.
   Accepts a `className` prop so the parent can apply animation styles. */

function detect(url) {
  try {
    const h = new URL(url).hostname.replace('www.', '')
    if (h.includes('instagram'))          return 'instagram'
    if (h.includes('youtube') ||
        h === 'youtu.be')                 return 'youtube'
    if (h.includes('vimeo'))              return 'vimeo'
    if (h.includes('twitter') ||
        h === 'x.com' ||
        h.startsWith('x.com'))            return 'twitter'
    if (h.includes('tiktok'))             return 'tiktok'
    if (h.includes('linkedin'))           return 'linkedin'
    if (h.includes('spotify'))            return 'spotify'
    if (h.includes('facebook') ||
        h === 'fb.com')                   return 'facebook'
    if (h.includes('behance'))            return 'behance'
    if (h.includes('github'))             return 'github'
    if (h.includes('imdb'))               return 'imdb'
    return 'globe'
  } catch {
    return 'globe'
  }
}

